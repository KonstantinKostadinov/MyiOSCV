    let headerId = "headerId"
    let personalData = AllData.personalInfo
    override func viewDidLoad() {
        super.viewDidLoad()
        navigationItem.title = "My iOS CV"
        collectionView.backgroundColor = UIColor.rgb(red: 240, green: 240, blue: 240)
        collectionView?.register(HomeControllerHeader.self, forSupplementaryViewOfKind: UICollectionView.elementKindSectionHeader, withReuseIdentifier: headerId)
        self.collectionView!.register(HomeControllerCell.self, forCellWithReuseIdentifier: cellId)
        collectionView.reloadData()
        // Do any additional setup after loading the view.
    }

